package UsClass;

public class MathDemo {
    public static void main(String[] args) {
        //返回大于等于 numvalue 的最小整数值
        System.out.println(Math.ceil(1.45));
        //返回小于等于 numvalue 的最大整数值
        System.out.println(Math.floor(1.45));
        System.out.println(Math.sqrt(9)); //返回平方根
        System.out.println(Math.abs(-41)); //返回绝对值
        System.out.println(Math.rint(3.25)); //返回最接近的整数
    }
}
