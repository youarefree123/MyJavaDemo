package Arithmetic;


/**
 * 计算类
 */
public class Calculate {

    /**
     * 加
     * @param a
     * @param b
     * @return
     */
    public int plus (int a,int b){
        return a+b;
    }

    /**
     * 减
     * @param a
     * @param b
     * @return
     */
    public int minus (int a,int b){
        return a-b;
    }

    /**
     * 乘
     * @param a
     * @param b
     * @return
     */
    public int mul (int a,int b){
        return a*b;
    }

    /**
     * 除
     * @param a
     * @param b
     * @return
     */
    public int Divide (int a,int b){
        return a/b;
    }
}